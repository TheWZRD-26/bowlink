export function translate(lang, path) {
  const parts = path.split(".");
  let cur = dict[lang] || dict.fr;
  for (const p of parts) {
    if (cur && typeof cur === "object" && p in cur) cur = cur[p];
    else return path;
  }
  return cur;
}

